/*******************************************************************
* 时间复杂度：O(M)
* 说明：
* 1. 求欧拉通路，支持重边、有向边
* 2. 使用前需要保证欧拉通路存在，且dfs从其端点开始
* 3. 欧拉通路存在当且仅当奇数度的结点有0个或2个
* 4. dfs后栈内为欧拉通路的倒序，需要进行翻转
*******************************************************************/
int vis[M];
vector<int> node[N];
vector<int> stk;

void dfs(int x)
{
    for (auto e : node[x])
    {
        if (vis[e.second]) continue;
        vis[e.second] = 1;
        dfs(e.first);
    }
    stk.push_back(x);
}