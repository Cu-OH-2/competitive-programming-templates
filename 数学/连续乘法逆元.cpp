/*******************************************************************
* 时间复杂度：O(n)
* 说明：
* 1.线性时间复杂度求[1,n]的逆元
* 2.注意mod必须与1-n所有数互质，否则不存在逆元
*******************************************************************/
struct ConInv
{
    vector<ll> inv;
    ConInv(int sz, ll mod)
    {
        inv.resize(sz);
        inv[1] = 1;
        for (int i = 2; i <= sz; ++i)
        {
            inv[i] = (mod - mod / i) * inv[mod % i] % mod;
        }
    }
};